const mongoose = require("mongoose");

const schema = new mongoose.Schema({
  googleId: {
    type: String,
    required: true,
    unique: true,
  },
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  profilePicture: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.model("googleUser", schema);
